// Helper function to get localized doctor data
export const getLocalizedDoctor = (doctor, locale = 'ar') => {
  return {
    ...doctor,
    name: locale === 'ar' ? doctor.name : doctor.nameEn,
    location: locale === 'ar' ? doctor.location : doctor.locationEn,
    experience: locale === 'ar' ? doctor.experience : doctor.experienceEn
  }
}

